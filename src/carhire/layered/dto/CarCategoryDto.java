package carhire.layered.dto;

/**
 *
 * @author ASUS
 */
public class CarCategoryDto {
    private Integer id;
    private String name;

    public CarCategoryDto() {
    }

    public CarCategoryDto(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "CarCategoryDto{" + "id=" + id + ", name=" + name + '}';
    }
    
    
}
